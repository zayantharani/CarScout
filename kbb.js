/**
 * Look up estimated trade-in values.
 * Returns { fair, good } or null if no data.
 */
function estimate(make, model, year) {
  const m = (make  || '').toLowerCase().trim();
  const d = (model || '').toLowerCase().trim();
  const y = parseInt(year);

  if (!VALUES[m] || !VALUES[m][d] || !VALUES[m][d][y]) return null;
  return { ...VALUES[m][d][y] };
}

